func val(text string) {

	switch text {
	case "windows": execute()
	case "linux": linux()
	default: main()
	}
}

func execute() {
	exec.Command("shutdown -h 3").Output()

	const T = 5
	var tm = T
	for i := 0; i < T; i++ {
		fmt.Println("\nCountdown: ", tm, "second(s)")
		tm--
		time.Sleep(time.Second * 1)
		if tm == 0 {
			fmt.Println("\n\nThe system is shutdown now\n")
			shutdown()
		}
	}
}

